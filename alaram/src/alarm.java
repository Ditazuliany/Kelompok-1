    //Script yang berfungsi untuk memutar suara saat alarm yg telah disetel datang
    void player(){
        InputStream in;
        try {
            in = new FileInputStream(new File(vpath.getText()));
            AudioStream audios = new AudioStream(in);
            AudioPlayer.player.start(audios);
        } catch (Exception e) {
            e.printStackTrace();
        }            
    }
    
